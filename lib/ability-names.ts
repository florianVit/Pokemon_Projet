export function getAbilityName(abilityName: string, language: "en" | "fr" = "en"): string {
  const translation = abilityTranslations[abilityName.toLowerCase()]
  if (!translation) {
    // Fallback: capitalize the original name
    return abilityName.split("-").map(word => word.charAt(0).toUpperCase() + word.slice(1)).join(" ")
  }
  return translation[language]
}
