// Get defensive effectiveness (what damages this type receives)
export function getDefensiveEffectiveness(defendingType: string): Record<string, number> {
  const effectiveness: Record<string, number> = {}
  
  // Check all attacking types
  Object.keys(TYPE_CHART).forEach(attackingType => {
    const chart = TYPE_CHART[attackingType]
    effectiveness[attackingType] = chart[defendingType] || 1
  })
  
  return effectiveness
}

// Get combined defensive effectiveness for dual types
export function getCombinedDefensiveEffectiveness(types: string[]): Record<string, number> {
  if (types.length === 0) return {}
  
  const effectiveness: Record<string, number> = {}
  
  // Get effectiveness for first type
  const firstTypeEffectiveness = getDefensiveEffectiveness(types[0])
  Object.keys(TYPE_CHART).forEach(type => {
    effectiveness[type] = firstTypeEffectiveness[type] || 1
  })
  
  // Multiply by second type effectiveness if exists
  if (types.length > 1) {
    const secondTypeEffectiveness = getDefensiveEffectiveness(types[1])
    Object.keys(effectiveness).forEach(type => {
      effectiveness[type] *= (secondTypeEffectiveness[type] || 1)
    })
  }
  
  return effectiveness
}

// Get offensive coverage (what types this type can hit effectively)
export function getOffensiveCoverage(attackingType: string): Record<string, number> {
  return TYPE_CHART[attackingType] || {}
}

